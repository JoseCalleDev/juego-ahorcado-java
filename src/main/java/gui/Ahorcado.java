    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed

        // Leer la letra ingresada por el usuario y convertirla a minúscula
        String letraIngresada = txtLetra.getText().toLowerCase();

        // Verificar si se ingresó solo una letra
        if (letraIngresada.length() == 1) {

            boolean letraCorrecta = false;

            // Iterar a través de la palabra oculta para buscar la letra ingresada
            for (int i = 0; i < palabraOculta.length(); i++) {
                // Si la letra es correcta, se actualiza la letra en la posición correspondiente
                if (Character.toLowerCase(palabraOculta.charAt(i)) == letraIngresada.charAt(0)) {
                    letrasAdivinadas[i] = palabraOculta.charAt(i);
                    letraCorrecta = true;
                }
            }

            // Actualizar el texto en la interfaz con las letras adivinadas hasta el momento
            letrasAgregadas = new String(letrasAdivinadas);
            lblLetras.setText(letrasAgregadas);
            txtLetra.setText("");

            //comprobar si se ganó
            String comprobar = lblLetras.getText();
            if (comprobar.equalsIgnoreCase(palabraOculta)) {

                //llamar metodo de victoria
                ganarPartida();

            }
            // Si la letra es incorrecta, actualizar el número de intentos y mostrar un mensaje
            if (!letraCorrecta && intentos <= 10) {
                JOptionPane.showMessageDialog(null, "Respuesta incorrecta. Te quedan " + (intentosMaximos - intentos) + " intentos",
                        "Intentos Restantes", JOptionPane.WARNING_MESSAGE);
                intentos++;
            }

            // Si se alcanza el número máximo de intentos, finalizar el juego
            if (intentos == (intentosMaximos + 1)) {
                enJuego = false;
            }

            if (enJuego == false) {
                perderPartida();

            }

            // Si el usuario no ingresa una sola letra, mostrar un mensaje de advertencia
        } else if (letraIngresada.length() > 1) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, ingresa solo una letra.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Por favor, ingresa una letra.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSiguienteActionPerformed

