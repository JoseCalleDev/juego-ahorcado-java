    public void darInicio() {
        
        //palabras posibles para el juego
        String palabras[] = {"mesa", "casa", "perro", "zapato", "calle", "sol", "luna", "flor", "ciudad", "escuela", "gente",
            "libro", "silla", "cama", "madre", "puerta", "amigo", "comida", "agua", "lluvia", "pelota", "rojo", "noche", "gato"};

        //sorteo de palabras
        Random palabraRandom = new Random();
        int indice = palabraRandom.nextInt(palabras.length);
        palabraOculta = palabras[indice];

        letrasAdivinadas = new char[palabraOculta.length()];

        for (int i = 0; i < letrasAdivinadas.length; i++) {
            letrasAdivinadas[i] = '-';
        }

        this.letrasAgregadas = new String(letrasAdivinadas);
        lblLetras.setText(letrasAgregadas);

    }

    public void ganarPartida() {

        JOptionPane.showMessageDialog(rootPane, "¡Felicidades, has ganado!", "¡Éxito!", JOptionPane.INFORMATION_MESSAGE);
        txtLetra.setText(" ");
        txtLetra.setEnabled(false);
        puntaje++;
        lblPuntaje.setText("+" + puntaje + "");
        btnSiguiente.setEnabled(false);
    }

    public void perderPartida() {
        JOptionPane.showMessageDialog(null, "¡Lo siento, has perdido! Intenta nuevamente.", "Error", JOptionPane.ERROR_MESSAGE);
        txtLetra.setText("");
        txtLetra.setEnabled(false);
        puntaje = 0;
        lblLetras.setText(":(");
        btnSiguiente.setEnabled(false);
    }

    public void alternarSonido() {

        if (isMute) {
            sound.loop();
            isPlaying = true;
        } else {
            sound.stop();
            isPlaying = false;
        }

        isMute = !isPlaying;

    }


